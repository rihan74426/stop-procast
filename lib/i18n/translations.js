// Replace/extend the t(...) helper so it supports both fallback string and vars object.
// Usage:
//   t('en', 'dash_title')                    -> basic lookup
//   t('en', 'welcome', 'Hi')                 -> fallback string
//   t('en', 'greeting', { name: 'Alex' })    -> placeholder replacement for "{{name}}"
export function t(lang, key, fallbackOrVars) {
  const isVars = typeof fallbackOrVars === "object" && fallbackOrVars !== null;
  const fallback = !isVars ? fallbackOrVars : undefined;
  const vars = isVars ? fallbackOrVars : undefined;

  let str = translations[lang]?.[key] ?? translations.en?.[key] ?? fallback ?? key;

  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{\\{${k}\\}\\}`, "g"), String(v));
    });
  }

  return str;
}
